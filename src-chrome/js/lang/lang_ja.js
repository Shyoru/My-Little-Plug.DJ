Lang.ready = function()
{
    RoomUser.minimumRolloverWidth = 150;
    Lobby.posLobbyLabels = function()
    {
        if ($.browser.mozilla)
        {
            $(".room-media").css("left", 38);
            $(".room-djs").css("bottom", 1);
            $(".room-djs-label").css("top", 1);
            $(".room-friends").css("bottom", 1);
            $(".room-friends-label").css("top", 1);
        }
        if (Main.isMac)
        {
            if (!$.browser.mozilla) $(".room-population-value").css("top", 11);
            else
            {
                $(".room-djs-label").css("top", 1);
                $(".room-friends-label").css("top", 1);
            }
        }
    };
    MediaOverlay.positionRoomProgress = function()
    {
        var t = (Main.isMac && $.browser.mozilla ? 128 : 126);
        $("#overlay-container").append(this.roomProgressMessage.css("left", Main.LEFT + 567).css("top", t));
    };
    Room.posUserName = function(span, textWidth, textHeight)
    {
        var j = Main.isMac ? !$.browser.mozilla ? 1 : 3 : 1;
        span.css("top", ~~(($("#user-name").height() - textHeight) / 2) + j);
        $("#user-name-edit-button").css("left", Math.min(203, 55 + textWidth));
    };
    RoomUser.posRolloverLabel = function()
    {
        $(".avatar-rollover-button span").css("top", $.browser.mozilla ? 3 : 0);
    };
    Dialog.posDialogFields = function()
    {
        if (Main.isMac)
        {
            if ($.browser.mozilla)
            {
                $(".dialog-header span").css("top", 7);
                $(".dialog-message").css("top", $(".dialog-message").css("top") + 1);
                $(".dialog-button span").css("top", 8);
                if (!Dialog.isRoomInfo) $(".dialog-input-label").css("top", 8);
                else $(".dialog-menu-container label").css("left", 48);
                $(".dialog-checkbox-container label").css("top", 1);
                $(".room-info-description-label").css("top", 155);
                $("#dialog-room-info .dialog-input-label").css("top", 1);
            }
            else
            {
                $(".dialog-header span").css("top", 4);
                $(".dialog-button span").css("top", 6);
                if (Dialog.isRoomInfo) $(".dialog-checkbox-container label").css("top", 1).css("left", 21);
                $(".dialog-menu-container label").css("left", 47).css("top", 4);
            }
            if (Main.isSafari)
            {
                $(".room-info-description-label").css("top", 154);
                $("#dialog-room-info .dialog-input-label").css("top", 3);
            }
        }
        if (!Dialog.isRoomInfo) $(".dialog-menu-container label").css("left", 0);
    };
    MediaOverlay.posImportLabels = function()
    {
        var t = 41;
        if (Main.isMac)
        {
            if ($.browser.mozilla)
            {
                t = 36;
                $("#media-import-youtube-button span").css("top", 6);
                $("#media-import-soundcloud-button span").css("top", 6);
                $("#tt-import-button span").css("top", 6).css("left", 8);
            }
            else t = 39;
        }
        else
        {
            if (Main.isChrome) t = 39;
            else if ($.browser.msie || Main.isSafari) t = 34;
        }
        $("#media-import-soundcloud-button").css("top", t);
        $("#media-import-youtube-button").css("top", t);
        $("#tt-import-button").css("top", t);
    };
    if (Main.isMac)
    {
        if (!$.browser.mozilla)
        {
            $(".chat-title").css("top", 3);
            $("#chat-bubble").css("margin-top", 2);
            $("#button-chat-sound span").css("top", 1);
            $("#current-room-info span").css("top", -1);
            $("#current-room-users span").css("top", -1);
            $("#current-room-history span").css("top", -1);
            $("#current-room-history").css("background-position", "0");
            $(".meta-header span").css("top", 3);
            $(".user-score-title").css("top", 12);
            $(".overlay-title").css("top", 10);
            $(".user-points-title").css("top", 13);
            $("#media-menu-your-playlists span").css("top", -2);
            $("#avatar-rollover-username").css("top", 3);
            $("#avatar-rollover-usertype").css("top", 21);
            $("#avatar-rollover-points").css("top", 38);
            $("#avatar-rollover-fans").css("top", 54);
            $("#avatar-rollover-plays").css("top", 70);
        }
        else
        {
            $(".chat-title").css("top", 8);
            $("#chat-bubble").css("margin-top", -3);
            $("#button-chat-sound span").css("top", 1);
            $("#current-room-info span").css("top", 0);
            $("#current-room-users span").css("top", 0);
            $("#current-room-history span").css("top", 0);
            $(".meta-header span").css("top", 4);
            $(".overlay-title").css("top", 17);
            $("#media-menu-your-playlists span").css("top", -1);
            $("#avatar-rollover-points").css("top", 39);
            $("#avatar-rollover-fans").css("top", 55);
            $("#avatar-rollover-plays").css("top", 71);
        }
    }
};
