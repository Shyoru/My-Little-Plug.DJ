Lang.ready = function()
{
    MediaOverlay.panelTitleOffset = 90;
    RoomUser.minimumRolloverWidth = 170;
    MediaOverlay.positionRoomProgress = function()
    {
        var t = (Main.isMac && $.browser.mozilla ? 135 : 133);
        $("#overlay-container").append(this.roomProgressMessage.css("left", Main.LEFT + 565).css("top", t));
    };
    Lobby.posLobbyLabels = function()
    {
        if ($.browser.mozilla)
        {
            $(".room-media").css("left", 76);
            $(".room-djs").css("bottom", 1);
            $(".room-djs-label").css("top", 1);
            $(".room-friends").css("bottom", 1);
            $(".room-friends-label").css("top", 1);
        }
        if (Main.isMac)
        {
            if (!$.browser.mozilla) $(".room-population-value").css("top", 11);
            else
            {
                $(".room-djs-label").css("top", 2);
                $(".room-friends-label").css("top", 2);
            }
        }
    };
}
